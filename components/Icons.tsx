interface ProfilePictureProps {
  src?: string | null;
  alt?: string;
  className?: string;
}

export const ProfilePicture: React.FC<ProfilePictureProps> = ({ src, alt, className }) => {
  const [hasError, setHasError] = useState(!src);

  useEffect(() => {
    setHasError(!src);
  }, [src]);

  if (hasError) {
    return (
      <div className={`flex items-center justify-center bg-gray-200 dark:bg-gray-700 ${className}`}>
        <UserCircleIcon className="w-3/4 h-3/4 text-gray-400 dark:text-gray-500" />
      </div>
    );
  }

  return (
    <img
      src={src!}
      alt={alt}
      className={className}
      onError={() => setHasError(true)}
    />
  );
};